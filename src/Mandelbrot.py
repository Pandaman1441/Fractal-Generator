from Fractal import Fractal

class Mandelbrot(Fractal):
    def __init__(self, fractalInfo):
        self.__fInfo = fractalInfo


    def getIteration(self, c):
        z = complex(0.0, 0.0)
        for i in range(self.__fInfo['iterations']):
            z = z * z + c
            if abs(z) > 2:
                return i
        return self.__fInfo['iterations'] - 1
    def getInfo(self):
        return self.__fInfo